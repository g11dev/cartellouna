package ComplexClasses;

public class Casting {
    public static void main(String[] args) {
        float f = (float) 4.5;
        double d = 8;
        int g = (int) 4.435737854;
        System.out.println(g);
        Long k = (long) (1000 + 599);
        System.out.println(k);
    }
}
