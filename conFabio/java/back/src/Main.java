public class Main {                                    // classe chiamata 'main'
    public static void main(String[] args) {           // public static void main(String[] args) -> inizio obbligatorio da mettere
        System.out.println("Buongiornissimo1!!!");     // PRINT = 'sout' per scriverlo con tab
        int a = 20 + 12*17;
        System.out.println(a);
        int b= 78%17;
        System.out.println(b);
        char c = 'k';                                   // char vale 1 solo carattere
        System.out.println(c);
        boolean d = 5>7;
        System.out.println(d);
        float e = 4.5f;                                 //per poter assegnare a double devo attaccare 'f' dopo il numero
        System.out.println(e);
        double f = 3.1415;
        System.out.println(f);
    }
}